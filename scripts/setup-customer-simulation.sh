#!/bin/bash
# =============================================================================
# HDIM Customer Simulation Setup Script
# =============================================================================
# This script sets up the complete customer simulation environment including:
# - External PostgreSQL databases
# - External HAPI FHIR server with sample data
# - HDIM minimal clinical portal services
#
# Prerequisites:
# - Docker and Docker Compose installed
# - Ports 5432, 8080, 8001, 8084-8087, 4200, 6379, 16686 available
#
# Usage:
#   chmod +x scripts/setup-customer-simulation.sh
#   ./scripts/setup-customer-simulation.sh
# =============================================================================

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_DIR="$(dirname "$SCRIPT_DIR")"

cd "$PROJECT_DIR"

echo "========================================"
echo "HDIM Customer Simulation Setup"
echo "========================================"
echo ""

# =============================================================================
# STEP 1: Setup Environment File
# =============================================================================
echo "[1/6] Setting up environment configuration..."

if [ ! -f .env ]; then
    cp .env.customer-simulation .env
    echo "  Created .env from .env.customer-simulation"
fi

# Update .env with required settings
cat > .env << 'EOF'
# HDIM Customer Simulation Environment
# Generated by setup-customer-simulation.sh

# ==============================================================================
# EXTERNAL POSTGRESQL
# ==============================================================================
POSTGRES_HOST=host.docker.internal
POSTGRES_PORT=5432
POSTGRES_USER=healthdata
POSTGRES_PASSWORD=healthdata_password
POSTGRES_DB=healthdata_db

# ==============================================================================
# EXTERNAL FHIR SERVER
# ==============================================================================
EXTERNAL_FHIR_URL=http://host.docker.internal:8080/fhir
FHIR_AUTH_TYPE=none

# ==============================================================================
# SERVICE VERSIONS
# ==============================================================================
VERSION=latest

# ==============================================================================
# AUTHENTICATION
# ==============================================================================
GATEWAY_AUTH_DEV_MODE=true
GATEWAY_AUTH_SIGNING_SECRET=dev-signing-secret-for-local-testing-only-minimum-32-chars
JWT_SECRET=hdim-dev-jwt-secret-key-for-local-development-only-64chars
JWT_EXPIRATION_MS=900000

# ==============================================================================
# REDIS (Docker)
# ==============================================================================
REDIS_HOST=redis
REDIS_PORT=6379

# ==============================================================================
# TRACING (Jaeger)
# ==============================================================================
OTEL_EXPORTER_OTLP_ENDPOINT=http://jaeger:4317

# ==============================================================================
# DEMO OPTIONS
# ==============================================================================
DEMO_AUTO_SEED=false
DEMO_MODE_ENABLED=false
DEMO_TENANT_ID=demo-tenant
EOF

echo "  Environment file configured"

# =============================================================================
# STEP 2: Start External PostgreSQL
# =============================================================================
echo ""
echo "[2/6] Starting external PostgreSQL..."

# Check if postgres container exists
if docker ps -a --format '{{.Names}}' | grep -q '^hdim-external-postgres$'; then
    echo "  PostgreSQL container exists, starting..."
    docker start hdim-external-postgres 2>/dev/null || true
else
    echo "  Creating new PostgreSQL container..."
    docker run -d \
        --name hdim-external-postgres \
        -e POSTGRES_USER=healthdata \
        -e POSTGRES_PASSWORD=healthdata_password \
        -e POSTGRES_DB=healthdata_db \
        -p 5432:5432 \
        postgres:15-alpine
fi

# Wait for PostgreSQL to be ready
echo "  Waiting for PostgreSQL to be ready..."
for i in {1..30}; do
    if docker exec hdim-external-postgres pg_isready -U healthdata > /dev/null 2>&1; then
        echo "  PostgreSQL is ready!"
        break
    fi
    sleep 1
done

# =============================================================================
# STEP 3: Create Databases
# =============================================================================
echo ""
echo "[3/6] Creating databases..."

DATABASES=(
    "gateway_db"
    "fhir_db"
    "patient_db"
    "quality_db"
    "caregap_db"
    "cql_db"
    "consent_db"
    "analytics_db"
    "hcc_db"
    "notification_db"
    "ecr_db"
    "cms_db"
    "qrda_db"
    "sdoh_db"
    "predictive_db"
    "prior_auth_db"
    "approval_db"
    "cost_db"
    "docs_db"
    "event_router_db"
    "event_processing_db"
    "ehr_connector_db"
    "demo_db"
    "sales_db"
    "agent_builder_db"
    "agent_runtime_db"
)

for db in "${DATABASES[@]}"; do
    docker exec hdim-external-postgres psql -U healthdata -d healthdata_db -c "CREATE DATABASE $db;" 2>/dev/null || true
done

echo "  Created ${#DATABASES[@]} databases"

# Add pg_trgm extension to key databases
echo "  Adding pg_trgm extension..."
for db in gateway_db fhir_db patient_db quality_db caregap_db; do
    docker exec hdim-external-postgres psql -U healthdata -d $db -c "CREATE EXTENSION IF NOT EXISTS pg_trgm;" 2>/dev/null || true
done

# =============================================================================
# STEP 4: Create Test Users in Gateway DB
# =============================================================================
echo ""
echo "[4/6] Creating test users..."

docker exec hdim-external-postgres psql -U healthdata -d gateway_db << 'EOSQL'
