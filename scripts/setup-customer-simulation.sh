    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    username VARCHAR(255) NOT NULL UNIQUE,
    email VARCHAR(255) NOT NULL UNIQUE,
    password_hash VARCHAR(255) NOT NULL,
    first_name VARCHAR(100),
    last_name VARCHAR(100),
    enabled BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Create user_roles table
CREATE TABLE IF NOT EXISTS user_roles (
    user_id UUID REFERENCES users(id),
    role VARCHAR(50) NOT NULL,
    PRIMARY KEY (user_id, role)
);

-- Create user_tenants table
CREATE TABLE IF NOT EXISTS user_tenants (
    user_id UUID REFERENCES users(id),
    tenant_id VARCHAR(100) NOT NULL,
    PRIMARY KEY (user_id, tenant_id)
);

-- Create refresh_tokens table
CREATE TABLE IF NOT EXISTS refresh_tokens (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID REFERENCES users(id),
    token VARCHAR(500) NOT NULL UNIQUE,
    expires_at TIMESTAMP WITH TIME ZONE NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Insert test users (password: password123 - BCrypt hash)
INSERT INTO users (username, email, password_hash, first_name, last_name)
VALUES
    ('test_admin', 'admin@hdim-demo.com', '$2a$10$N9qo8uLOickgx2ZMRZoMyeIjZRGdjGj/9n78H1rkGk1uVz.NmyMKC', 'Admin', 'User'),
    ('test_evaluator', 'evaluator@hdim-demo.com', '$2a$10$N9qo8uLOickgx2ZMRZoMyeIjZRGdjGj/9n78H1rkGk1uVz.NmyMKC', 'Evaluator', 'User'),
    ('demo_admin', 'demo@demo-tenant.com', '$2a$10$N9qo8uLOickgx2ZMRZoMyeIjZRGdjGj/9n78H1rkGk1uVz.NmyMKC', 'Demo', 'Admin')
ON CONFLICT (username) DO NOTHING;

-- Assign roles
INSERT INTO user_roles (user_id, role)
SELECT id, 'ADMIN' FROM users WHERE username = 'test_admin'
ON CONFLICT DO NOTHING;

INSERT INTO user_roles (user_id, role)
SELECT id, 'EVALUATOR' FROM users WHERE username = 'test_evaluator'
ON CONFLICT DO NOTHING;

INSERT INTO user_roles (user_id, role)
SELECT id, 'ADMIN' FROM users WHERE username = 'demo_admin'
ON CONFLICT DO NOTHING;

-- Assign tenants
INSERT INTO user_tenants (user_id, tenant_id)
SELECT id, 'demo-tenant' FROM users WHERE username IN ('test_admin', 'test_evaluator', 'demo_admin')
ON CONFLICT DO NOTHING;

INSERT INTO user_tenants (user_id, tenant_id)
SELECT id, 'demo-tenant' FROM users WHERE username = 'test_admin'
ON CONFLICT DO NOTHING;
EOSQL

echo "  Test users created"

# =============================================================================
# STEP 5: Start External HAPI FHIR Server
# =============================================================================
echo ""
echo "[5/6] Starting external HAPI FHIR server..."

# Remove existing container if any
docker rm -f external-fhir-server 2>/dev/null || true

# Start HAPI FHIR with in-memory H2
docker run -d \
    --name external-fhir-server \
    -p 8080:8080 \
    -e hapi.fhir.fhir_version=R4 \
    -e hapi.fhir.allow_external_references=true \
    -e hapi.fhir.allow_multiple_delete=true \
    -e spring.datasource.url=jdbc:h2:mem:hapi \
    -e spring.datasource.driverClassName=org.h2.Driver \
    -e spring.jpa.hibernate.ddl-auto=update \
    -e JAVA_TOOL_OPTIONS="-Xms512m -Xmx2g" \
    hapiproject/hapi:latest

echo "  HAPI FHIR server starting (takes ~2 minutes)..."
echo "  You can check status with: curl http://localhost:8080/fhir/metadata"

# =============================================================================
# STEP 6: Start HDIM Clinical Portal Services
# =============================================================================
echo ""
echo "[6/6] Starting HDIM clinical portal services..."

docker compose -f docker-compose.minimal-clinical.yml up -d

echo ""
echo "========================================"
echo "Setup Complete!"
echo "========================================"
echo ""
echo "Services starting up. Check status with:"
echo "  docker ps --filter 'name=hdim'"
echo ""
echo "Service URLs (after startup ~2-3 minutes):"
echo "  - Clinical Portal:    http://localhost:4200"
echo "  - Gateway API:        http://localhost:8001"
echo "  - FHIR Service:       http://localhost:8085/fhir"
echo "  - Patient Service:    http://localhost:8084/patient"
echo "  - Quality Measure:    http://localhost:8087/quality-measure"
echo "  - Care Gap:           http://localhost:8086/care-gap"
echo "  - External FHIR:      http://localhost:8080/fhir"
echo "  - Jaeger Tracing:     http://localhost:16686"
echo ""
echo "Test Users (password: password123):"
echo "  - test_admin / admin@hdim-demo.com (ADMIN)"
echo "  - test_evaluator / evaluator@hdim-demo.com (EVALUATOR)"
echo "  - demo_admin / demo@demo-tenant.com (ADMIN)"
echo ""
echo "To load sample FHIR data after HAPI FHIR is ready:"
echo "  ./scripts/load-sample-fhir-data.sh"
echo ""
